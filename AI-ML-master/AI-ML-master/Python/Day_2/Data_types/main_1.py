# Sequential data types -- List, Tuples, Dictionaries


#List -- items within a square brackets.

a = [1,2.7,'Amit',4+8j,True]

print(a)
print(type(a))

#Tuples -- items within parentheses.

b = (1,2.7,'Amit',4+8j,True)

print(b)
print(type(b))


c = {'key':'Value', "Name":"John","Age":30,"Address":"xyzpur"}

